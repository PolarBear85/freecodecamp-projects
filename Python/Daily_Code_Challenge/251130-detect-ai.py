def detect_ai(text):

    words = text.split(" ")
    long_words_count = 0
    char_counts = {}

    for word in words:
        if len(word) >= 7:
            long_words_count += 1

        for char in word:
            char_counts[char] = char_counts.get(char,0)+1

    if char_counts.get("-", 0) >= 2:
            return "AI"
    if char_counts.get("(", 0) >= 2:
        return "AI"
    if long_words_count >= 3:
        return "AI"

    return "Human"

